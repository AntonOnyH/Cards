import UIKit

extension UIColor {
    
    struct CardColor {
        static let black: UIColor = UIColor(named: "C1")!
        static let darkGray: UIColor = UIColor(named: "C2")!
        static let criene: UIColor = UIColor(named: "C3")!
        static let pearlWhite: UIColor = UIColor(named: "C4")!
        static let yoloYellow: UIColor = UIColor(named: "C5")!
        static let blue: UIColor = UIColor(named: "ShopCardC1")!
        static let navy: UIColor = UIColor(named: "ShopCardC2")!
    }
    
    struct BackgroundColor {
        static let dark: UIColor = UIColor(named: "BackgroundC2")!
        static let mid: UIColor = UIColor(named: "BackgroundC1")!
        static let extraDark: UIColor = UIColor(named: "SegmentBackgroundColor")!
    }
    
    struct GradiantColor {
        static let gradLight: UIColor = UIColor(named: "CardC1")!
        static let gradDark: UIColor = UIColor(named: "CardC2")!
    }
}
